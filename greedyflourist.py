def compute(c, x):
    return sum(c) * (x + 1)

def minimum_cost(k, c, x):
    n =  len(c)
    cost = 0
    if k >= n:
        return compute(c, x)
    cost += compute(c[n-k:], x)
    cost += minimum_cost(k, c[:n-k], x+1)
    return cost

